ORG 256
 MOV    AH,9            ; print title
 LEA    DX,[txt_title]
 INT    21H

 FNINIT                 ; fill FPU stack
 FLDLG2
 FLD    ST0
 FADD   ST0,ST0
 FLDLN2
 FLD    ST0
 FADD   ST0,ST0
 FLDL2E
 FLD    ST0
 FADD   ST0,ST0
 FLDPI

 XOR    AX,AX           ; set up illegal handler
 MOV    DS,AX
 MOV    SI,[6 * 4 + 0]
 MOV    DI,[6 * 4 + 2]
 MOV    AX,CS
 MOV    WORD [6 * 4 + 0],illegal
 MOV    WORD [6 * 4 + 2],AX
 MOV    DS,AX
 MOV    ES,AX
 MOV    [iloffset],SI
 MOV    [ilsegment],DI

 MOV    [stdstack],SP
 MOV    [pointer],tab

next:
 MOV    BX,[pointer]    ; pick next table item
 ADD    [pointer],2
 MOV    DI,[BX]
 TEST   DI,DI
 JZ     quit
 MOV    [pointer2],DI
 MOV    [chars],-2
 LEA    SI,[txt_pre]
 CALL   Print
 MOV    SI,DI
 CALL   Print
 LEA    SI,[txt_wait]
 CALL   Print
next2:
 MOV    DI,[pointer2]   ; pick next table item
 MOV    CX,-1

 MOV    AL,'$'          ; skip title
 REPNZ  SCASB

 TEST   WORD [DI],CX
 JZ     next
 MOV    [jump],DI       ; perform test

again:
 MOV    AL,0C3H         ; skip code
 REPNZ  SCASB
 MOV    [pointer2],DI   ; perform test

 MOV    SI,DI
@@:
 LODSB
 CMP    AL,'$'
 JE     @F
 CMP    AL,'z'
 JNA    @B
 JMP    again
@@:

 CALL   [jump]
 JZ     fail

okay:
 LEA    SI,[txt_okay]
 CALL   Print
 JMP    next2

illegal:
 MOV   SP,[stdstack]    ; reset stack, then fail

fail:
 MOV    SI,[pointer2]
 CALL   Print
 LEA    SI,[txt_fail]
 CALL   Print
 JMP    next2

quit:

 MOV    SI,[iloffset]   ; restore illegal handler
 MOV    DI,[ilsegment]
 XOR    AX,AX
 MOV    DS,AX
 MOV    WORD [6 * 4 + 0],SI
 MOV    WORD [6 * 4 + 2],DI

 FNINIT                 ; empty FPU stack
 MOV    AH,0            ; wait for keypress
 INT    16H

 MOV    AX,4C00H        ; exit
 INT    21H
;-------------------------------------------------------
; Functions

Print:
; SI: string
 MOV    CX,[chars]
.1:
 LODSB
 CMP    AL,"$"
 JE     .3
 CMP    CX,79
 JNL    .2
 XCHG   AX,DX
 MOV    AH,2
 INT    21H
.2:
 INC    CX
 JMP    .1
.3:
 MOV    [chars],CX
RETN

